public class If {
    public static void main(String[] args) {
        int x = 20;
        int y = 18;
        
        if (x > y) {
            System.out.println("É maior que y");
        } else if (x == y) {
            System.out.println("É igual a y");
        } else {
            System.out.println("É menor que y");
        }
        
    }
}
